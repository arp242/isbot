func TestBotUA(t *testing.T) {
	var fail []string
	for _, b := range bots {
		r := &http.Request{Header: make(http.Header)}
		r.Header.Add("User-Agent", b)
		if IsNot(Bot(r)) {
			fail = append(fail, b)
		}
	}
	if len(fail) > 0 {
		t.Errorf("%d failed:\n%s", len(fail), strings.Join(fail, "\n"))
	}
}

func TestNotBotUA(t *testing.T) {
	var fail []string
	for _, b := range notBots {
		r := &http.Request{Header: make(http.Header)}
		r.Header.Add("User-Agent", b)
		if Is(Bot(r)) {
			fail = append(fail, b)
		}
	}
	if len(fail) > 0 {
		t.Errorf("%d failed:\n%s", len(fail), strings.Join(fail, "\n"))
	}
}

func TestDup(t *testing.T) {
	for _, list := range [][]string{bots, notBots} {
		list = list[:]
		sort.Strings(list)
		var last string
		for i := range list {
			if list[i] == last {
				t.Errorf("duplicate: %s", list[i])
			}
			last = list[i]
		}
	}
}

